def propager(x,y,etiquette):
    global matriceEtiquette
    if(x<0 or y<0 or y>len(matriceEtiquette[0])-1 or x>len(matriceEtiquette)-1):
        return
    if(matriceEtiquette[x][y]==0):
        matriceEtiquette[x][y]=etiquette
        propager(x - 1, y,etiquette)
        propager(x + 1, y,etiquette)
        propager(x, y - 1,etiquette)
        propager(x, y + 1,etiquette)

def trouverZoneAPropager(*etiquettes):
    global matriceEtiquette
    etiquettesUtilises=0
    for i in range (2,len(matriceEtiquette)-1):
        for j in range (1,len(matriceEtiquette[i])-1):
            if(matriceEtiquette[i][j]==0 and matriceEtiquette[i-1][j]==1 and matriceEtiquette[i-2][j]==0 and (matriceEtiquette[i-1][j-1]==1 or matriceEtiquette[i][j-1]==1 or matriceEtiquette[i+1][j-1]==1) and (matriceEtiquette[i-1][j+1]==1 or matriceEtiquette[i][j+1]==1 or matriceEtiquette[i+1][j+1]==1)):
                propager(i, j,etiquettes[etiquettesUtilises])
                etiquettesUtilises+=1

